<h3>Los platos son los siguientes : </h3><br>
<h4>1. Sandwich de Chola (sándwich de cerdo)</h4>
<img src="{{asset('nuevo/cho.jpg') }}">
<p>"Es un pan relleno de jamón, pero ya sabes, el jamón que ha cocido a fuego lento hasta que esté crujiente. - Con un bonito encurtido de cebolla y ají" El pan suave, piel de cerdo crujiente y salsa picante es, como afirma Seidler, deliciosos .</p>

<h4>2. Anticucho (ensartado corazones de carne)</h4>
<img src="{{asset('nuevo/anti.jpg') }}">
<p>Comer con los dedos tienden a ser un poco incómodo, así que afortunadamente anticucho sólo se come por la noche. "Es más o menos una brocheta de corazón de res con papas", dice Seidler. "Es a la parrilla, así que tienes un montón de sabor de las llamas y los aromas de la barbacoa." Por supuesto, este plato no es nada sin su pegajosa, salsa de maní picante, y, oscilando en US $ 1 la porción, es uno de los platos bolivianos más baratos.</p>

<h4>3. Silpancho</h4>
<img src="{{asset('nuevo/silpancho.jpg') }}">
<p>Este plato viene de Cochabamba, de los valles de Bolivia. Dice Seidler, "Es como una enorme Milanesa, muy muy plana hecha de carne de res o de llama que está empanado." La carne empanada es servida con una ensalada de remolacha y zanahoria cubierto con huevos fritos, arroz y papas. "Tienes casi todo en un plato, es un montón de comida", dice Seidler.</p>

<h4>4. Ají de Fideos (lengua de ternera picante)</h4>
<img src="{{asset('nuevo/fideo.jpg') }}">
<p>"No estamos hablando de un plato realmente picante, como un picante asiático, mezclado en una salsa", aclara Seidler, al describir la salsa de ají amarillo se utiliza para cocinar el plato tradicional de la lengua de ternera. "Estamos hablando uno de sabores aromáticos bolivianos muy cálidos, mucho más suave y apenas picante." Una vez que la lengua de ternera se cocina en el la salsa, se sirve junto con chuño, que es la papa deshidratada acompañada de la salsa clásica boliviana de tomate, cebolla y cilantro. Dice Seidler, "La salsa añade un poco de frescura al mezclar la sal pesada."</p>



